		//__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, rcc);

		HAL_Delay(100);
		if (rcc > TIM2->ARR)
			rcc = 0;
		else
			rcc += 50;
		//HAL_GPIO_TogglePin(GLD0_GPIO_Port, GLD0_Pin);

